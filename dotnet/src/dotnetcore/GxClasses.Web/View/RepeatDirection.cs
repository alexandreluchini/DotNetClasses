namespace GeneXus.WebControls
{
	public enum RepeatDirection
	{
		//
		// Summary:
		//     Items of a list are displayed horizontally in rows from left to right, then top
		//     to bottom, until all items are rendered.
		Horizontal = 0,
		//
		// Summary:
		//     Items of a list are displayed vertically in columns from top to bottom, and then
		//     left to right, until all items are rendered.
		Vertical = 1
	}
}